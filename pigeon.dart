//////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////
class MediaItem {
  String? title;
  String? artist;
  String? album;
  String? artUri;
  double? position;
  double? duration;
  bool? playing;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////
@HostApi()
abstract class MediaSessionProtocol {
  void setMedia(MediaItem item);
  void setActiveCommands(List<MediaCommand> commands);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////
enum MediaCommand {
  pause,
  play,
  stop,
  togglePlayPause,
  nextTrack,
  previousTrack,
  repeatMode,
  shuffleMode,
  playBackRate,
  seekBackward,
  seekForward,
  skipBackward,
  skipForward,
  position,
  rating,
  like,
  dislike,
  bookmark,
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
enum MediaRepeatType { none, one, all }

enum MediaShuffleType { none, items, collections }

//////////////////////////////////////////////////////////////////////////////////////////////////////////
class MediaNotification {
  MediaCommand command;
  Object? value;
  MediaNotification({required this.command, this.value});
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////
@FlutterApi()
abstract class MediaNotificationHandler {
  void notify(MediaNotification notification);
}

